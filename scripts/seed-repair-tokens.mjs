#!/usr/bin/env node
// scripts/seed-repair-tokens.mjs — one-shot import of the n8n repair_tokens
// data table into D1.
//
// Usage:
//   1. In n8n, open the "repair_tokens" data table → Export → CSV.
//      Expected columns (header order doesn't matter, but names do):
//        AppNumber, parentTaskId, mail, token, lastEmailSentAt, lastEmailType
//      The `mail` column is intentionally ignored — owner email lives in
//      muse-reviews from now on.
//   2. node scripts/seed-repair-tokens.mjs <path-to-csv> > seed.sql
//   3. wrangler d1 execute muse --remote --file=seed.sql
//
// Token values must round-trip verbatim or existing email magic links break.

import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';

const path = process.argv[2];
if (!path) {
  console.error('usage: node scripts/seed-repair-tokens.mjs <csv>');
  process.exit(1);
}

const raw = readFileSync(resolve(path), 'utf-8');
const lines = raw.split(/\r?\n/).filter((l) => l.length > 0);
if (lines.length < 2) {
  console.error('CSV has no data rows');
  process.exit(1);
}

function splitCsv(line) {
  const out = [];
  let cur = '';
  let q = false;
  for (let i = 0; i < line.length; i++) {
    const ch = line[i];
    if (q) {
      if (ch === '"' && line[i + 1] === '"') { cur += '"'; i++; }
      else if (ch === '"') q = false;
      else cur += ch;
    } else {
      if (ch === ',') { out.push(cur); cur = ''; }
      else if (ch === '"') q = true;
      else cur += ch;
    }
  }
  out.push(cur);
  return out;
}

function sql(value) {
  if (value === null || value === undefined || value === '') return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}

const header = splitCsv(lines[0]).map((h) => h.trim());
const idx = (name) => {
  const i = header.indexOf(name);
  if (i === -1) throw new Error(`CSV missing column: ${name}`);
  return i;
};
const iAppNumber = idx('AppNumber');
const iParent = idx('parentTaskId');
const iToken = idx('token');
const iLastSent = header.indexOf('lastEmailSentAt');
const iLastType = header.indexOf('lastEmailType');

const nowIso = new Date().toISOString();
console.log('-- Auto-generated by scripts/seed-repair-tokens.mjs');
console.log(`-- Source: ${path}`);
console.log(`-- Generated: ${nowIso}`);
// Note: D1 rejects raw SQL BEGIN/COMMIT — wrangler executes the file as a
// single batch which is already atomic.

for (let li = 1; li < lines.length; li++) {
  const cols = splitCsv(lines[li]);
  const appNumberRaw = (cols[iAppNumber] ?? '').trim();
  const parentTaskId = (cols[iParent] ?? '').trim();
  const token = (cols[iToken] ?? '').trim();
  if (!parentTaskId || !token) continue;

  const appNumber = appNumberRaw && /^-?\d+$/.test(appNumberRaw) ? appNumberRaw : 'NULL';
  const lastSent = iLastSent >= 0 ? (cols[iLastSent] ?? '').trim() : '';
  const lastType = iLastType >= 0 ? (cols[iLastType] ?? '').trim() : '';

  console.log(
    `INSERT INTO repair_tokens (parent_task_id, token, app_number, last_email_sent_at, last_email_type, created_at) VALUES (${sql(parentTaskId)}, ${sql(token)}, ${appNumber === 'NULL' ? 'NULL' : appNumber}, ${sql(lastSent)}, ${sql(lastType)}, ${sql(nowIso)}) ON CONFLICT(parent_task_id) DO UPDATE SET token = excluded.token, app_number = excluded.app_number, last_email_sent_at = excluded.last_email_sent_at, last_email_type = excluded.last_email_type;`,
  );
}

